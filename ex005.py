# Faça um programa que leia um número Inteiro e mostre na tela o seu sucessor e seu antecessor.

n = int(input('Digite um numero: '))
print(f'Analisando o valor {n}, o numero antecessor é {n-1} e o sucessor é {n+1}.')

### ou

n = int(input('Digite um numero: '))
n1 = n - 1
n2 = n + 1
print(f'Analisando o valor {n}, o numero antecessor é {n1} e o sucessor é {n2}.')